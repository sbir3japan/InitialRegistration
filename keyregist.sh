#!/bin/bash

# --- 設定 ---
KEY_PATH="$HOME/.ssh/id_ed25519_temporary"
COMMENT="temporary-access-key"

# 1. 既存の同名鍵があれば削除（上書き確認を避けるため）
if [ -f "$KEY_PATH" ]; then
    rm "$KEY_PATH" "$KEY_PATH.pub"
fi

# 2. 鍵ペアの生成 (Ed25519は短くてQRコード化に最適)
# パスフレーズなし(-N "")で生成
ssh-keygen -t ed25519 -C "$COMMENT" -f "$KEY_PATH" -N "" > /dev/null

echo "----------------------------------------------------------"
echo "鍵ペアの生成が完了しました。"
echo "以下のQRコードをスマホ等でスキャンして公開鍵をコピーしてください。"
echo "----------------------------------------------------------"

# 3. 公開鍵をQRコードでターミナルに表示
# -t ansiutf8: ターミナル上で読み取り可能な形式で出力
# -l L: 誤り訂正レベルを低くし、QRの密度を下げてスキャンしやすくする
qrencode -t ansiutf8 -l L < "${KEY_PATH}.pub"

echo "----------------------------------------------------------"
echo "公開鍵の生テキスト:"
cat "${KEY_PATH}.pub"
echo "----------------------------------------------------------"
